// Generate suggestion strings from street objects
function buildSuggestions(): { label: string; district: string }[] {
  const suggestions: { label: string; district: string }[] = [];

  for (const street of streets) {
    // Primary: full name as-is
    suggestions.push({ label: street.name, district: street.district });

    // Short alias — e.g. "ул. Баумана" → "Баумана"
    for (const t of street.types) {
      const prefix = street.name.split(" ")[0].toLowerCase();
      if (["улица", "проспект", "переулок", "набережная", "микрорайон", "площадь", "посёлок"].includes(prefix)) {
        const shortName = street.name.replace(/^(улица|проспект|переулок|набережная|микрорайон|площадь|посёлок|жилой комплекс)\s+/i, "");
        if (shortName && !suggestions.some((s) => s.label === shortName)) {
          suggestions.push({ label: shortName, district: street.district });
        }
      }
    }
  }

  return suggestions;
}

